void EA_DOG128_Write_CMD(unsigned char cmd)
{
    unsigned char i;
    LCD_NCS_LOW;
    LCD_A0_LOW;
    LCD_SCL_LOW;

    for(i = 0; i < 8; i++){
        LCD_SCL_LOW;
        if(cmd & BIT7){
            LCD_SDI_HIGH;
        }else{
            LCD_SDI_LOW;
        }
        LCD_SCL_HIGH;
        cmd <<= 1;
    }
    LCD_NCS_HIGH;
    LCD_A0_HIGH;
}

void EA_DOG128_Write_Dat(unsigned char dat)
{
    unsigned char i;
    LCD_NCS_LOW;
    LCD_A0_HIGH;
    LCD_SCL_LOW;

    for(i = 0; i < 8; i++){
        LCD_SCL_LOW;
        if(dat & BIT7){
            LCD_SDI_HIGH;
        }else{
            LCD_SDI_LOW;
        }
        LCD_SCL_HIGH;
        dat <<= 1;
    }
    LCD_NCS_HIGH;
    LCD_A0_LOW;
}

void EA_DOG128_Clear(void)
{
    unsigned char i, j;

    for(i = 0; i < 8; i++){
        EA_DOG128_Write_CMD(0xB0 + i);
        EA_DOG128_Write_CMD(0x10);
        EA_DOG128_Write_CMD(0x00);
        for(j = 0; j < 128; j++){
            EA_DOG128_Write_Dat(0);
        }
    }
}

void EA_DOG128_Set_Addr(unsigned char x, unsigned char y)
{
    unsigned char i;

    i = x & 0x0F;
    x = (x >>  4) + 0x10;

    EA_DOG128_Write_CMD(0xB0 + y);
    EA_DOG128_Write_CMD(x);
    EA_DOG128_Write_CMD(i);
}

